# Basic arithmetic operations
print(5 + 4)  # Output: 9
print(5 - 10)  # Output: -5
print(5 * 10)  # Output: 50
print(10 / 2)  # This gives 5.0 (floating-point division)

# Exponentiation (power)
print(10 ** 5)  # 10 to the power of 5 -> (10 * 10 * 10 * 10 * 10) -> Output: 100000

# Floor division
print(10 // 2)  # This gives 5 (integer division, no decimal part)

"""
Example:
print(5 / 6)  # Output: 0.8333333~
But if we use floor division:
print(5 // 6)  # Output: 0 (because 5 divided by 6 is less than 1)
"""

# Modulus operator (remainder)
print(10 % 3)  # Output: 1
"""
Explanation:
10 / 3 = 3 (quotient)
3 * 3 = 9
10 - 9 = 1 (remainder is 1)
"""

# Parentheses in arithmetic expressions
print(5 + 5 * 2)  # Output: 15 (multiplication happens first)
print((5 + 5) * 2)  # Output: 20 (addition happens first due to parentheses)

# Comparison operators
print(10 >= 5)  # Output: True (10 is greater than or equal to 5)

# Average calculation
print((1 + 2 + 3 + 4 + 5 + 6 + 7) / 7)  # Output: 4.0

