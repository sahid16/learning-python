# Variables and basic operations
result = 10 + 5
print(result)  # Output: 15

result2 = result / 2
print(result2)  # Output: 7.5

# Updating a variable with itself
num1 = 10
num1 = num1 + 10
print(num1)  # Output: 20

# Shortcut for adding to the same variable
num2 = 10
num2 += 5
print(num2)  # Output: 15

# Division using shortcut operator
_num3 = 20
_num3 /= 2
print(_num3)  # Output: 10.0
print(_num3)  # Output: 10.0
print(_num3)  # Output: 10.0           
